package com.homs.demo.model;

public class Auth {
    String name;
    String email;
    String password;
    String role;
    
    public Auth() {
        
    }
    public String getName() {
        return name;
    }
    public String getEmail() {
        return email;
    }
    public String getPassword() {
        return password;
    }
    public String getRole() {
        return role;
    }
}
